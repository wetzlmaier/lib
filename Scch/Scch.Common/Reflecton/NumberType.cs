namespace Scch.Common.Reflecton
{
    /// <summary>
    /// Defines the type of a number.
    /// </summary>
    public enum NumberType
    {
        /// <summary>
        /// No number.
        /// </summary>
        NoNumber,
        /// <summary>
        /// Floating point number.
        /// </summary>
        FloatingPoint,
        /// <summary>
        /// Signed integer number.
        /// </summary>
        SignedInt,
        /// <summary>
        /// Unsigned integer number.
        /// </summary>
        UnsignedInt
    }
}
